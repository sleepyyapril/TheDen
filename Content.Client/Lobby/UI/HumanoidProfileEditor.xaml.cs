        {
            if (Profile == null || !_entManager.EntityExists(PreviewDummy))
                return;

            if (_entManager.TryGetComponent<HumanoidAppearanceComponent>(PreviewDummy, out var humanoid))
            {
                var hiddenLayers = humanoid.HiddenLayers;
